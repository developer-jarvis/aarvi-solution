<script>
    $(document).ready(function () {
        $('#app_formsubmit').on('click', function (event) {
            event.preventDefault();

            // Reset all error messages
            $(".error").text("");

            // Business Type Validation
            var businessType = $("#business_type").val();
            if (businessType.split(' ').length < 3) {
                $(".business_error").text("Please Enter Proper Details");
            }

            // Website URL Validation
            var website = $("#website").val();
            if (website.trim() !== "" && !isValidURL(website)) {
                $(".website_error").text("Please enter a valid URL.");
            }

            // Name Validation
            var name = $("#Name").val();
            if (name.split(' ').length < 3) {
                $(".name_error").text("Please Enter Full Name");
            }

            // Phone Number Validation
            var phone = $("#phone").val();
            if (!isValidPhoneNumber(phone)) {
                $(".phone_error").text("Enter Valid Phone no.");
            }

            // Message Validation
            var message = $("#message").val();
            if (message.split(' ').length < 5) {
                $(".massage_error").text("Please tell us more");
            }

            // Check if there are any errors
            if ($(".error:empty").length === $(".error").length) {
                // If all validations pass, proceed with AJAX submission
                submitForm();
            }
        });

        function submitForm() {
            // Show preloader when AJAX request starts
            $('#preloaderOverlay').show();

            const formData = $("#mobile_app").serialize();

            // Send the AJAX request to the PHP script
            $.ajax({
                type: 'POST',
                url: 'include/app_validate.php',
                data: formData,
                success: function (response) {
                    Swal.fire({
                        icon: "success",
                        title: "Thank You!",
                        text: "Thank You for Choosing Us, be patient we will contact You soon..",
                        confirmButtonColor: "#3085d6",
                        confirmButtonText: "OK"
                    });
                    $('#mobile_app')[0].reset();
                },
                error: function (xhr, status, error) {
                    Swal.fire({
                        icon: "error",
                        title: "Oops...",
                        text: "Something went wrong, please try again.",
                        confirmButtonColor: "#3085d6",
                        confirmButtonText: "OK"
                    });
                },
                complete: function () {
                    // Hide preloader when AJAX request stops
                    $('#preloaderOverlay').hide();
                }
            });
        }

        function isValidURL(url) {
            // Use a regular expression for URL validation
            var urlRegex = /^(ftp|http|https):\/\/[^ "]+$/;
            return urlRegex.test(url);
        }

        function isValidPhoneNumber(phone) {
            // Use a regular expression for 10-digit phone number validation
            var phoneRegex = /^[6-9]\d{9}$/;
            return phoneRegex.test(phone);
        }
    });
</script>



